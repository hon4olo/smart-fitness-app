const normalize = (value: string) => value.toLowerCase().normalize('NFKD').replace(/[\u0300-\u036f]/g, '').replace(/[^a-z0-9]+/g, ' ').trim();

export const foodCatalogLookup = foodCatalog.reduce((lookup, food) => {
  const lookupKeys = [food.id, food.name, ...food.aliases];
  lookupKeys.forEach((key) => {
    lookup.byId.set(normalize(key), food);
    lookup.byName.set(normalize(key), food);
  });
  food.tags.forEach((tag) => lookup.byTag.set(normalize(tag), food));
  return lookup;
}, { byId: new Map<string, FoodCatalogItem>(), byName: new Map<string, FoodCatalogItem>(), byTag: new Map<string, FoodCatalogItem>() });

export const foodCatalogByCategory = foodCategoryOrder.reduce((groups, category) => {
  groups[category] = foodCatalog.filter((food) => food.category === category);
  return groups;
}, {} as Record<FoodCategory, FoodCatalogItem[]>);

